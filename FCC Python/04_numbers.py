from math import *

# Operations
print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)

# Modulus
print(10 % 3) # Returns 1

# Parentheses
print(3 * (4 - 5)) # Returns -3

# Exponent
print(pow(4, 2)) # Returns 16

# Absolute value
print(abs(-6)) # Returns 6

#max/min
print(max(10, 100)) # Returns 100
print(min(1, 100)) # Returns 1

# Round
print(round(3.8)) # Rounds to 4
print(round(3.1)) # Rounds to 3

# Floor
print(floor(3.8)) # Always rounds down
print(floor(3.8)) # Always rounds up


# Square Root
print(sqrt(36)) # Returns 6

# Converting num to str
print("I am " + str(26) + " years old.")